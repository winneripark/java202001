package 반복문;

public class 연습문제1 {
			// for문으로 1부터 10까지 프린트
	public static void main(String[] args) {
		int j = 10;
		while (j > 0 ) {
			//for(초기값; 조건식; 증감식)
			System.out.print(j + " ");
			j--;
		}

		System.out.println();
		
		for (int i = 1; i < 11; i++) {
			//for(초기값; 조건식; 증감식)
			System.out.print(i + " ");
		}		
		
	}

}
