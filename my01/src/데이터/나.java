package 데이터;

public class 나 {

	public static void main(String[] args) {
		// 기본 데이터 연습
		// 저장공간 => 변수
		// 저장공간명 => 변수명
		int 나이 = 100; // 정수 4byte
		double 시력 = 0.8; // 실수 8byte
		char 성별 = '남'; // 문자 한 글자 2byte
		boolean 저녁 = true; // 논리 1byte
		
		// 자동완성 기능
		// syso/sysout + crtl + spacebar
		
		System.out.println("내 나이는 " + 나이 + "세");
		System.out.println("내 시력은 " + 시력);
		System.out.println("내 성별은 " + 성별);
		System.out.println("나는 저녁식사를 했습니다 - " + 저녁);
		
		// 실행(run) 단축키는 crtl + f11 key
	}

}
