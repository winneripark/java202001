package 데이터;

public class 기본데이터2 {

	public static void main(String[] args) {
		// 기본 데이터 저장
		// 정수, 실수 저장
		
	    int x = 250;
	    // int 정수 
	    //컴퓨터에서 정수는 -21억 ~ 21억 까지 사용가능함
	    
	    double y = 5.5;
	    // double 실수
	    
	    char z = '한';
	    // char 문자
	    // 한개의 문자일 경우에는 ' 사용
	    		
	    boolean p = true;
	    // boolean 참 거짓
	    // true 참, false 거짓
	    
	    
	    System.out.println("x:" + x);
	    System.out.println("y:" + y);
	    System.out.println("z:" + z);
	    System.out.println("p:" + p);
		
	}

}
